# Allow the user to enter the first and second numbers
first_number = float(input("Enter the first number: "))
second_number = float(input("Enter the second number: "))

# Perform five mathematical operations with the input numbers
addition = first_number + second_number
subtraction = first_number - second_number
division = first_number / second_number
multiplication = first_number * second_number
square = first_number ** 2 + second_number ** 2

# Print all five results to the console
print("Sum:", addition)
print("Difference:", subtraction)
print("Division:", division)
print("Multiplication:", multiplication)
print("Square and sum:", square)
